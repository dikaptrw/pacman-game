// Check if a cell is walkable (not a wall)
export const isWalkable = (cell: Cell | null): boolean => {
  if (!cell) return false;
  return cell.type !== "wall";
};

// Get a cell from the maze
export const getCell = (
  maze: Cell[][],
  col: number,
  row: number
): Cell | null => {
  if (row < 0 || row >= maze.length || col < 0 || col >= maze[0].length) {
    return null;
  }
  return maze[row][col];
};

// Count dots in the maze
export const countDots = (maze: Cell[][]): number => {
  let dots = 0;
  maze.forEach((row) => {
    row.forEach((cell) => {
      if (cell.type === "dot" || cell.type === "powerPellet") {
        dots++;
      }
    });
  });
  return dots;
};
